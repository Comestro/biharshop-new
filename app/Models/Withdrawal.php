<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Withdrawal extends Model
{
    protected $guarded = [];

    protected $casts = [
        'amount' => 'decimal:2',
        'details' => 'array',
    ];

    public function membership()
    {
        return $this->belongsTo(Membership::class);
    }
}
